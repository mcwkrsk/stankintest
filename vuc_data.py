from datetime import date, timedelta
import logging

# Словарь событий: ключ = (группа, дата в формате 'YYYY-MM-DD'), значение = список предметов
_schedule = {}

def _add_event(group: str, day: int, month: int, subject: str):
    """Добавляет одно событие для указанной группы и даты"""
    date_obj = date(2026, month, day)
    key = (group, date_obj.isoformat())
    if key not in _schedule:
        _schedule[key] = []
    if subject not in _schedule[key]:
        _schedule[key].append(subject)

def _add_events_for_group(group: str, dates_with_subjects):
    """dates_with_subjects: список кортежей (день, месяц, предмет)"""
    for day, month, subject in dates_with_subjects:
        _add_event(group, day, month, subject)

