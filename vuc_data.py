# Функция получения расписания на неделю (остаётся без изменений)
def get_events_for_week(start_date: date) -> dict:
    """
    Возвращает словарь:
        ключ – дата,
        значение – список кортежей (группа, список предметов)
    для всех групп на неделю от start_date.
    """
    result = {}
    for i in range(7):
        current = start_date + timedelta(days=i)
        date_str = current.isoformat()
        events = []
        for (group, d_str), subjects in _schedule.items():
            if d_str == date_str:
                events.append((group, subjects))
        if events:
            result[current] = events
    return result

